/*
    Operadores são símbolos usados para realizar operações com valores e variáveis, como cálculos matemáticos, comparações e atribuições.

    Eles são fundamentais para controlar a lógica dos programas.
*/

let a = 10;
let b = 2;

// --------- Exemplo - Soma ---------

let soma = a + b;

console.log("O resultado da operação de soma é igual: " + soma);


// --------- Exemplo - Subtração ---------

let subtracao = a - b;

console.log("O resultado da operação de subtração é igual: " + subtracao);


// --------- Exemplo - Multiplicação ---------

let multiplicacao = a * b;

console.log("O resultado da operação de multiplicação é igual: " + multiplicacao);


// --------- Exemplo - Divisão ---------

let divisao = a / b;

console.log("O resultado da operação de divisão é igual: " + divisao);


// --------- Exemplo - Potenciação ---------

let potenciacao = a ** b;

console.log("O resultado da operação de potenciação é igual: " + potenciacao);


// --------- Exemplo - Resto de Divisão ---------

let resto = a % b;

console.log("O resultado da operação de resto de divisão é igual: " + resto);
